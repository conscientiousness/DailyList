import UIKit

struct CustomColors{
    static func getBackgroundColor() -> UIColor{
        return UIColor(hex: "3D3D3D", alpha: 1)
    }
    static func getMainColor() -> UIColor{
        return UIColor(hex: "FFC317", alpha: 1)
    }
    static func getTitleGreenColor() -> UIColor{
        return UIColor(hex: "004773", alpha: 1)
    }
    static func getGreyColor() -> UIColor{
        return UIColor(hex: "6E6E6D", alpha: 1)
    }
    static func getLightGreyColor() -> UIColor{
        return UIColor(hex: "A4A4A4", alpha: 1)
    }
    static func getLightGreenColor() -> UIColor{
        return UIColor(hex: "2ECC87", alpha: 1)
    }
    static func getDarkBlueColor() -> UIColor{
        return UIColor(hex: "004773", alpha: 1)
    }
    static func getTextFieldBgGreyColor() -> UIColor{
        return UIColor(hex: "F1F4F9", alpha: 1)
    }
}